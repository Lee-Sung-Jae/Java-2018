public class Rectangle38 {
	private int x, y, width, height;

	Rectangle38() {
		this(0, 0, 1, 1);
	}

	Rectangle38(int width, int height) {
		this(0, 0, width, height);
	}

	Rectangle38(int x, int y, int width, int height) {
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
}
